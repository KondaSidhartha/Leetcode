//Function to find the data of nth node from the end of a linked list.
class Solution{
public:
    int getNthFromLast(Node *head, int n)
    {
         Node *temp=head;
    	int l=0;
    	while(temp!=NULL){
    	    l++;
    	    temp=temp->next;
    	}
    	if(n>l)return -1;
    	
    	l=l-n;
    	while(l--){
    	    head=head->next;
    	}
    	return head->data;
    }
};



